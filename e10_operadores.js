// Operador mayor que y menor que.
console.log(" Seis es mayor que cinco: " + 6 > 5)
console.log("Seis es menor que cinco: " + 6 < 5)
// Comparar cadenas de caracteres. 
console.log("A" > "B") // Utiliza el ordena alfabético
console.log("La letra B és menor que la letra C: " + ("B" < "C"))
console.log("ACB" > "ABC")
// Comparar variables
var a = 5
var b = 6
console.log("El valor de a es mayor que el valor de b: " + (a > b))
// Operador mayor o igual que
console.log(5 > 5) // false
console.log(5 >= 5) // true
// Operador menor o igual que
console.log(5 < 5) // false
console.log(5 <= 5) // true